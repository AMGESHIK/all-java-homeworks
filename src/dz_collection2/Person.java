package dz_collection2;

public class Person {
    public int id, age;
    public String post, name, surname, patronymic, group, department;

    public Person(int id, int age, String post, String name, String surname, String patronymic, String group, String department) {
        this.id = id;
        this.age = age;
        this.post = post;
        this.name = name;
        this.surname = surname;
        this.patronymic = patronymic;
        this.group = group;
        this.department = department;
    }


}
